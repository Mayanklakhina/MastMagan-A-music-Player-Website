
      var audio = document.getElementById("audio");
      var img = document.getElementById("theImage");

      document.addEventListener("click", function(e){
         if( e.target === img ){
           audio.src = "/Music/Chor Denge.mp3";
           audio.play();
         }
         else{
           audio.pause();
         }
      },false);


